;Funciones a hacer: MOVEMENT, CHECK_RANGE, PLANE_RENDER, MOVE

MOVE
LDI R1, KEYBOARD_INPUT

A_MOVE
LD R6 , ASCII_A
AND R1, R1, R5
AND R1, R1, R6
BRz MOVE_LEFT

W_MOVE
LD R6 , ASCII_W
AND R1, R1, R5
AND R1, R1, R6
BRz MOVE_UP

D_MOVE
LD R6 , ASCII_D
AND R1, R1, R5
AND R1, R1, R6
BRz MOVE_RIGHT

S_MOVE
LD R6 , ASCII_S
AND R1, R1, R5
AND R1, R1, R6
BRz MOVE_DOWN

MOVE_UP
ADD R3, R3, #1

 

MOVEMENT
LD R5 MASK
LDI R4, INPUT_DETECTOR
BRn MOVE


PLANE_RENDER



